Write-Host "üöÄ Starting backend..." -ForegroundColor Yellow

# Ki·ªÉm tra Java
Write-Host "`nüîç Checking Java..." -ForegroundColor Blue
try {
    $javaVersion = java -version 2>&1
    Write-Host "Java version: $javaVersion" -ForegroundColor Green
} catch {
    Write-Host "‚ùå Java not found!" -ForegroundColor Red
    exit 1
}

# Ki·ªÉm tra Maven
Write-Host "`nüîç Checking Maven..." -ForegroundColor Blue
try {
    $mavenVersion = mvn -version 2>&1
    Write-Host "Maven version: $mavenVersion" -ForegroundColor Green
} catch {
    Write-Host "‚ùå Maven not found!" -ForegroundColor Red
    Write-Host "Trying to use Maven wrapper..." -ForegroundColor Yellow
    
    if (Test-Path "mvnw.cmd") {
        Write-Host "Found Maven wrapper!" -ForegroundColor Green
        $mavenCmd = ".\mvnw.cmd"
    } else {
        Write-Host "‚ùå Maven wrapper not found!" -ForegroundColor Red
        exit 1
    }
} else {
    $mavenCmd = "mvn"
}

# Compile project
Write-Host "`nüîç Compiling project..." -ForegroundColor Blue
try {
    & $mavenCmd clean compile
    Write-Host "‚úÖ Compilation successful!" -ForegroundColor Green
} catch {
    Write-Host "‚ùå Compilation failed!" -ForegroundColor Red
    Write-Host "Error: $($_.Exception.Message)" -ForegroundColor Red
    exit 1
}

# Start backend
Write-Host "`nüîç Starting backend..." -ForegroundColor Blue
try {
    & $mavenCmd spring-boot:run
} catch {
    Write-Host "‚ùå Failed to start backend!" -ForegroundColor Red
    Write-Host "Error: $($_.Exception.Message)" -ForegroundColor Red
    exit 1
}
